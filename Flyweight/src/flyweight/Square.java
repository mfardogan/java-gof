package flyweight;

/**
 *
 * @author mfard
 */
public class Square {

    private int x, y;
    private String color;

    public Square() {
    }

    public Square(int x, int y, String color) {
        this.x = x;
        this.y = y;
        this.color = color;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String getColor() {
        return color;
    }

    public void draw() {
        System.out.println(color + " renkli kare: " + x + ", " + y);
    }
}
